# == , != , > , < , >= , <=
a , b , c = 2 , 2 , 8
# the relation operator ==
print(a==b) # True
print(a==c) # False
# the relation operator !=
print(a!=b) # False
print(a!=c) # True
# the relation operator > 
print(a>b) # False
# the relation operator <
print(a < b) # False
# the relation operator >=
print(a >= b) # True
print(a >= c) # False
# the relation operator <=
print(a <= b) # True
print(a <= c) # True 